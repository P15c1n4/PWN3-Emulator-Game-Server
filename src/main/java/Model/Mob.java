package Model;

public class Mob {
    private String mobName;
    private String mobId;
    private String mobCoord;
    private String MobTypeId;
    private int mobHp;
    /**
     * @return the mobName
     */
    public String getMobName() {
        return mobName;
    }

    /**
     * @param mobName the mobName to set
     */
    public void setMobName(String mobName) {
        this.mobName = mobName;
    }

    /**
     * @return the mobId
     */
    public String getMobId() {
        return mobId;
    }

    /**
     * @param mobId the mobId to set
     */
    public void setMobId(String mobId) {
        this.mobId = mobId;
    }

    /**
     * @return the mobCoord
     */
    public String getMobCoord() {
        return mobCoord;
    }

    /**
     * @param mobCoord the mobCoord to set
     */
    public void setMobCoord(String mobCoord) {
        this.mobCoord = mobCoord;
    }

    /**
     * @return the MobTypeId
     */
    public String getMobTypeId() {
        return MobTypeId;
    }

    /**
     * @param MobTypeId the MobTypeId to set
     */
    public void setMobTypeId(String MobTypeId) {
        this.MobTypeId = MobTypeId;
    }

    /**
     * @return the mobHp
     */
    public int getMobHp() {
        return mobHp;
    }

    /**
     * @param mobHp the mobHp to set
     */
    public void setMobHp(int mobHp) {
        this.mobHp = mobHp;
    }

    
    
    
    
}
